package dao.coupon;

public class couponInfo {
private int coID;
private String coName;
private int coDiscount;
private int copID;

public int getCoID() {
	return coID;
}
public void setCoID(int coID) {
	this.coID = coID;
}
public String getCoName() {
	return coName;
}
public void setCoName(String coName) {
	this.coName = coName;
}
public int getCoDiscount() {
	return coDiscount;
}
public void setCoDiscount(int coDiscount) {
	this.coDiscount = coDiscount;
}
public int getCopID() {
	return copID;
}
public void setCopID(int copID) {
	this.copID = copID;
}
}
