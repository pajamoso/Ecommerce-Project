package dao.payment;

public class paymentInfo {
	
	int payid;
	int cartId;
	double tbilla;
	double tbilld;
	
	public int getPayid() {
		return payid;
	}
	public void setPayid(int payid) {
		this.payid = payid;
	}
	public int getCartId() {
		return cartId;
	}
	public void setCartId(int cartId) {
		this.cartId = cartId;
	}
	public double getTbilla() {
		return tbilla;
	}
	public void setTbilla(double tbilla) {
		this.tbilla = tbilla;
	}
	public double getTbilld() {
		return tbilld;
	}
	public void setTbilld(double tbilld) {
		this.tbilld = tbilld;
	}
	

}
